package operators;

public class Arithmetic {

	public static void main(String[] args) {
		int a= 15, b= 25;
		//addition
		System.out.println("a+b="+(a+b));
		//Subtraction
		System.out.println("a-b="+(a-b));
		//multiplication
		System.out.println("a*b="+(a*b));
		//division
		System.out.println("a/b="+(a/b));
		//modulo (Remainder)
		System.out.println("a%b="+(a%b));
	}

}
