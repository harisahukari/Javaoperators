package operators;

public class Relational {

	public static void main(String[] args) {
		int a = 10;
		int b = 15;
		
		//== operator
		System.out.println(a==b);
		// != operator
		System.out.println(a!=b);
		//> operator
		System.out.println(a>b);
		//< operator
		System.out.println(a<b);
		//>= operator
		System.out.println(a>=b);
		//<= operator
		System.out.println(a<=b);
		

	}

}
